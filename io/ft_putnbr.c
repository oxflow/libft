#include <libft.h>

void			ft_putnbr(int n)
{
	if (n >= 0)
	{
		if (n >= 10)
		{
			ft_putnbr(n / 10);
			ft_putnbr(n % 10);
		}
		else
			ft_putchar(n + 48);
	}
	else
	{
		ft_putchar('-');
		ft_putnbr(-n);
	}
}
