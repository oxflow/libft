#include <stddef.h>

size_t		ft_intlen(int n)
{
	size_t	i;

	i = 1;
	if (n >= 0)
	{
		while (n >= 10)
		{
			i++;
			n /= 10;
		}
		return (i);
	}
	return (ft_intlen(-n) + 1);
}
