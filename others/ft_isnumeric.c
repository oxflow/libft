#include <libft.h>

int				ft_isnumeric(char *s)
{
	int			i;
	int			len;

	if (s[0] == '-')
		i = 1;
	else
		i = 0;
	len = ft_strlen(s);
	while (i < len)
	{
		if (!(s[i] >= 48 && s[i] < 58))
			return (0);
		i++;
	}
	return (1);
}
