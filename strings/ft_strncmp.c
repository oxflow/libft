#include <string.h>

int			ft_strncmp(char const *s1, char const *s2, size_t len)
{
	size_t	i;

	i = 0;
	while (s1[i] == s2[i] && s1[i] != '\0' && i < len)
		i++;
	if (i > 0 && i == len && (s1[i] == '\0' || s2[i] == '\0'))
		i--;
	return (s1[i] - s2[i]);
}
